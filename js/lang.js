let currentLang = localStorage.getItem('lang') || 'en';

i18next.init({
  lng: currentLang,
  resources
}, function(err, t) {
  updateContent();
  updateDirection();
  updateToggleButton();
});

function changeLanguage() {
  currentLang = currentLang === 'en' ? 'ar' : 'en';
  localStorage.setItem('lang', currentLang);

  if (currentLang === 'en') {
    document.body.classList.remove('rtl');
    document.body.classList.add('ltr');
  } else {
    document.body.classList.remove('ltr');
    document.body.classList.add('rtl');
  }  

  i18next.changeLanguage(currentLang, () => {
    updateContent();
    updateToggleButton();
    updateDirection();
    changeLanguageBtn();
  });
}

function changeLanguageBtn(){
const prevBtn = document.querySelector(".nav.prev");
const nextBtn = document.querySelector(".nav.next");

  if(currentLang === 'en') {
    prevBtn.innerHTML = "&#10094;";
    nextBtn.innerHTML = "&#10095;"; 
  } else {
    prevBtn.innerHTML = "&#10095;";
    nextBtn.innerHTML = "&#10094;"; 
  }
}

function updateContent() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.innerHTML = i18next.t(key);
  });
}

function updateToggleButton() {
  const btn = document.getElementById('langToggle');
  if (btn) btn.querySelector('.lang-text').textContent = currentLang.toUpperCase();
}

function updateDirection() {
  document.body.dir = (currentLang === 'ar') ? 'rtl' : 'ltr';
}

function updateContent() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const translated = i18next.t(key).replace(/\n/g, '<br>');
      el.innerHTML = translated;
    });
  }
  

document.getElementById('langToggle').addEventListener('click', changeLanguage);
